def 排序并输出病人信息(病人列表):
    # 将日期字符串转换为datetime对象以便排序
    for 病人 in 病人列表:
        病人['time'] = datetime.strptime(病人['time'], '%Y-%m-%d')
    
    # 按时间排序
    排序后列表 = sorted(病人列表, key=lambda x: x['time'])
    
    # 输出排序后的信息
    for 病人 in 排序后列表:
        print(f"姓名: {病人['name']}, 日期: {病人['time'].strftime('%Y-%m-%d')}, 温度: {病人['温度']}")

